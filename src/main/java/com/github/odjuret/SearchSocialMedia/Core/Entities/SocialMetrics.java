package com.github.odjuret.SearchSocialMedia.Core.Entities;

public class SocialMetrics {
    private Object comments;
    private Object dislikes;
    private Object favourites;
    private Object likes;
    private Object reach;
    private Integer shares;
    private Object views;
    private Object replies;
    public Object getComments() {
        return comments;
    }
    public void setComments(Object comments) {
        this.comments = comments;
    }
    public Object getDislikes() {
        return dislikes;
    }
    public void setDislikes(Object dislikes) {
        this.dislikes = dislikes;
    }
    public Object getFavourites() {
        return favourites;
    }
    public void setFavourites(Object favourites) {
        this.favourites = favourites;
    }
    public Object getLikes() {
        return likes;
    }
    public void setLikes(Object likes) {
        this.likes = likes;
    }
    public Object getReach() {
        return reach;
    }
    public void setReach(Object reach) {
        this.reach = reach;
    }
    public Integer getShares() {
        return shares;
    }
    public void setShares(Integer shares) {
        this.shares = shares;
    }
    public Object getViews() {
        return views;
    }
    public void setViews(Object views) {
        this.views = views;
    }
    public Object getReplies() {
        return replies;
    }
    public void setReplies(Object replies) {
        this.replies = replies;
    }
}
